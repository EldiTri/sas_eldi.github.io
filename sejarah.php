    <p class="card-text">Sejarah lahirnya Pancasila berawal dari kekalahan Jepang pada Perang Pasifik, tahun 1945. Menyadari kekalahan sudah di depan mata, Jepang berusaha menarik simpati rakyat Indonesia dengan menjanjikan kemerdekaan dan membentuk sebuah lembaga guna mempersiapkan kemerdekaan Indonesia.

Pada Tanggal 29 April 1945, “Dokuritsu Junbi Cosakai” atau Badan Penyelidik Usaha-Usaha Persiapan Kemerdekaan Indonesia (BPUPKI) resmi dibentuk. Dalam sejarahnya, BPUPKI menjalankan sidang pertamanya secara resmi pada tanggal 29 Mei-1 Juni 1945. Dalam sidang BPUPKI ini, sejumlah tokoh menyampaikan pidatonya terkait perumusan asas dasar negara. Selanjutnya, pada tanggal 1 Juni 1945, Soekarno memperkenalkan 5 sila, yang terdiri dari Kebangsaan Indonesia, Internasionalisme atau Peri Kemanusiaan, Mufakat atau Demokrasi, Kesejahteraan Sosial, dan Ketuhanan Yang Maha Esa. momen ini menjadi momen pertama dimana Pancasila diperkenalkan.

BPUPKI lalu membentuk Panitia Sembilan untuk merumuskan lebih rinci tentang rumusan Pancasila sebagai dasar negara. Tokoh tokoh Panitia Sembilan itu adalah:1. Ir. Soekarno, 2. Drs. Mohammad Hatta, 3. Mr. A. A. Maramis, 4. Mr. Muhammad Yamin, 5. Achmad Soebardjo, 6. Abikoesno Tjokrosoejoso, 7. Abdul Kahar Muzakkar, 8. H. Agus Salim, dan 9. K.H Abdul Wahid Hasyim. Hasil pembahasan Panitia Sembilan tertuang dalam Piagam Jakarta, 22 Juni 1945 sebagai berikut: 1. Ketuhanan dengan kewajiban menjalankan syariat Islam bagi pemeluk-pemeluknya, 2. Kemanusiaan yang adil dan beradab, 3. Persatuan Indonesia, 4. Kerakyatan yang dipimpin oleh hikmat kebijaksanaan dalam permusyawaratan. Namun masih terjadi perdebatan, J Latuharhary menyampaikan keberatan terutama kewajiban melakukan syariat bagi pemeluk-pemeluknya.</p>
    
